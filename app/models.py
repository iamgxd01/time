class List(db.Model):
    __tablename__ = 'lists'
    id = db.Column(db.Integer, primary_key=True)
    listname = db.Column(db.String(64),)
    list_order = db.Column(db.Integer)
    tasks = db.relationship('Task', backref='lists')

    def __repr__(self):
        return '<List %r>' % self.listname


